package usuarios.model;


public class Usuario {

		private String userId;
		private String rol;
		
		//Getters and setters
		public String getUserId() {
			return userId;
		}
		public void setUserId(String userId) {
			this.userId = userId;
		}
		public String getRol() {
			return rol;
		}
		public void setRol(String rol) {
			this.rol = rol;
		}
		
		
}
